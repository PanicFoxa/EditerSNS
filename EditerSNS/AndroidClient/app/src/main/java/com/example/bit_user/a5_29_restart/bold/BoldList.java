public class BoldList {

    float size;
    int imagesrc;
    String ptype;

    public BoldList(float size, int imagesrc, String ptype) { this.size = size; this.imagesrc = imagesrc; this.ptype = ptype;}
    public float getSize() { return size; }
    public void setSize(float size) { this.size = size; }
    public int getImagesrc() { return imagesrc; }
    public void setImagesrc(int imagesrc) { this.imagesrc = imagesrc; }
    public String getPtype() { return ptype; }
    public void setPtype(String ptype) { this.ptype = ptype; }

}
